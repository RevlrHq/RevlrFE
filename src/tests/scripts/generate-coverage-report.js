#!/usr/bin/env node

/**
 * Coverage Report Generator
 *
 * This script generates comprehensive test coverage reports for the dashboard
 * components, including detailed analysis, trends, and recommendations.
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

// Coverage thresholds
const COVERAGE_THRESHOLDS = {
    statements: 80,
    branches: 75,
    functions: 80,
    lines: 80,
};

// Component categories for detailed analysis
const COMPONENT_CATEGORIES = {
    'Dashboard Core': [
        'src/app/dashboard/',
        'src/components/StatisticsOverview',
        'src/components/DashboardCustomizer',
    ],
    'Charts & Analytics': [
        'src/components/charts/',
        'src/components/RevenueReporting',
        'src/components/EventPerformanceAnalytics',
        'src/components/AttendeeAnalytics',
    ],
    'Event Management': [
        'src/components/EnhancedEventTable',
        'src/components/EventTableRow',
        'src/components/EventTableModals',
    ],
    'Hooks & Services': ['src/hooks/useOrganizer', 'src/services/'],
    Utilities: ['src/lib/utils/', 'src/tests/utils/'],
};

class CoverageReportGenerator {
    constructor() {
        this.coverageData = null;
        this.reportPath = path.join(process.cwd(), 'coverage');
        this.outputPath = path.join(
            this.reportPath,
            'dashboard-coverage-report.md'
        );
    }

    /**
     * Generate comprehensive coverage report
     */
    async generateReport() {
        console.log('🔍 Generating dashboard coverage report...');

        try {
            // Run tests with coverage
            await this.runTestsWithCoverage();

            // Load coverage data
            await this.loadCoverageData();

            // Generate detailed report
            const report = await this.createDetailedReport();

            // Write report to file
            await this.writeReport(report);

            // Display summary
            this.displaySummary();

            console.log('✅ Coverage report generated successfully!');
            console.log(`📄 Report saved to: ${this.outputPath}`);
        } catch (error) {
            console.error(
                '❌ Error generating coverage report:',
                error.message
            );
            process.exit(1);
        }
    }

    /**
     * Run tests with coverage collection
     */
    async runTestsWithCoverage() {
        console.log('🧪 Running tests with coverage...');

        const testCommand = [
            'npx jest',
            '--coverage',
            '--coverageReporters=json',
            '--coverageReporters=lcov',
            '--coverageReporters=html',
            '--coverageReporters=text',
            '--testPathPattern=dashboard',
            '--passWithNoTests',
        ].join(' ');

        try {
            execSync(testCommand, {
                stdio: 'inherit',
                cwd: process.cwd(),
            });
        } catch (error) {
            throw new Error(`Test execution failed: ${error.message}`);
        }
    }

    /**
     * Load coverage data from JSON report
     */
    async loadCoverageData() {
        const coverageJsonPath = path.join(
            this.reportPath,
            'coverage-final.json'
        );

        if (!fs.existsSync(coverageJsonPath)) {
            throw new Error(
                'Coverage data not found. Make sure tests ran successfully.'
            );
        }

        try {
            const coverageJson = fs.readFileSync(coverageJsonPath, 'utf8');
            this.coverageData = JSON.parse(coverageJson);
        } catch (error) {
            throw new Error(`Failed to parse coverage data: ${error.message}`);
        }
    }

    /**
     * Create detailed coverage report
     */
    async createDetailedReport() {
        const timestamp = new Date().toISOString();
        const summary = this.calculateSummary();
        const categoryAnalysis = this.analyzeCoverageByCategory();
        const fileAnalysis = this.analyzeFilesCoverage();
        const recommendations = this.generateRecommendations(
            summary,
            fileAnalysis
        );

        return `# Dashboard Test Coverage Report

Generated on: ${timestamp}

## Executive Summary

${this.formatSummarySection(summary)}

## Coverage by Category

${this.formatCategoryAnalysis(categoryAnalysis)}

## Detailed File Analysis

${this.formatFileAnalysis(fileAnalysis)}

## Recommendations

${this.formatRecommendations(recommendations)}

## Coverage Trends

${this.formatTrends()}

## Test Quality Metrics

${this.formatTestQualityMetrics()}

## Action Items

${this.formatActionItems(summary, fileAnalysis)}

---

*Report generated by Dashboard Coverage Analyzer*
`;
    }

    /**
     * Calculate overall coverage summary
     */
    calculateSummary() {
        const files = Object.values(this.coverageData);
        const totals = {
            statements: { covered: 0, total: 0 },
            branches: { covered: 0, total: 0 },
            functions: { covered: 0, total: 0 },
            lines: { covered: 0, total: 0 },
        };

        files.forEach((file) => {
            Object.keys(totals).forEach((metric) => {
                if (file[metric]) {
                    totals[metric].covered += file[metric].covered || 0;
                    totals[metric].total += file[metric].total || 0;
                }
            });
        });

        const percentages = {};
        Object.keys(totals).forEach((metric) => {
            const { covered, total } = totals[metric];
            percentages[metric] = total > 0 ? (covered / total) * 100 : 0;
        });

        return {
            totals,
            percentages,
            fileCount: files.length,
            thresholdsMet: this.checkThresholds(percentages),
        };
    }

    /**
     * Analyze coverage by component category
     */
    analyzeCoverageByCategory() {
        const categoryResults = {};

        Object.entries(COMPONENT_CATEGORIES).forEach(([category, patterns]) => {
            const categoryFiles = this.getFilesByPatterns(patterns);
            const categoryCoverage =
                this.calculateCoverageForFiles(categoryFiles);

            categoryResults[category] = {
                ...categoryCoverage,
                fileCount: categoryFiles.length,
                files: categoryFiles.map((file) => ({
                    path: file,
                    coverage: this.getFileCoverage(file),
                })),
            };
        });

        return categoryResults;
    }

    /**
     * Analyze individual file coverage
     */
    analyzeFilesCoverage() {
        const files = Object.keys(this.coverageData);
        const dashboardFiles = files.filter(
            (file) =>
                file.includes('dashboard') ||
                file.includes('organizer') ||
                file.includes('statistics') ||
                file.includes('revenue') ||
                file.includes('event')
        );

        return dashboardFiles
            .map((filePath) => {
                const coverage = this.coverageData[filePath];
                const metrics = {};

                ['statements', 'branches', 'functions', 'lines'].forEach(
                    (metric) => {
                        if (coverage[metric]) {
                            const { covered, total } = coverage[metric];
                            metrics[metric] = {
                                covered,
                                total,
                                percentage:
                                    total > 0 ? (covered / total) * 100 : 0,
                            };
                        }
                    }
                );

                return {
                    path: filePath.replace(process.cwd(), ''),
                    metrics,
                    uncoveredLines: this.getUncoveredLines(coverage),
                    complexity: this.estimateComplexity(coverage),
                };
            })
            .sort((a, b) => {
                const aAvg = this.getAverageCoverage(a.metrics);
                const bAvg = this.getAverageCoverage(b.metrics);
                return aAvg - bAvg; // Sort by lowest coverage first
            });
    }

    /**
     * Generate recommendations based on coverage analysis
     */
    generateRecommendations(summary, fileAnalysis) {
        const recommendations = [];

        // Overall coverage recommendations
        Object.entries(summary.percentages).forEach(([metric, percentage]) => {
            const threshold = COVERAGE_THRESHOLDS[metric];
            if (percentage < threshold) {
                recommendations.push({
                    type: 'coverage',
                    priority: 'high',
                    metric,
                    current: percentage.toFixed(1),
                    target: threshold,
                    message: `${metric} coverage is below threshold (${percentage.toFixed(1)}% < ${threshold}%)`,
                });
            }
        });

        // File-specific recommendations
        const lowCoverageFiles = fileAnalysis.filter(
            (file) => this.getAverageCoverage(file.metrics) < 60
        );

        lowCoverageFiles.slice(0, 5).forEach((file) => {
            recommendations.push({
                type: 'file',
                priority: 'medium',
                file: file.path,
                coverage: this.getAverageCoverage(file.metrics).toFixed(1),
                message: `Low coverage in ${file.path} (${this.getAverageCoverage(file.metrics).toFixed(1)}%)`,
            });
        });

        // Test quality recommendations
        const filesWithManyUncoveredLines = fileAnalysis.filter(
            (file) => file.uncoveredLines.length > 20
        );

        filesWithManyUncoveredLines.forEach((file) => {
            recommendations.push({
                type: 'quality',
                priority: 'low',
                file: file.path,
                uncoveredLines: file.uncoveredLines.length,
                message: `Many uncovered lines in ${file.path} (${file.uncoveredLines.length} lines)`,
            });
        });

        return recommendations;
    }

    /**
     * Format summary section
     */
    formatSummarySection(summary) {
        const { percentages, thresholdsMet } = summary;
        const status = thresholdsMet.all ? '✅ PASSING' : '❌ FAILING';

        return `
**Overall Status: ${status}**

| Metric | Coverage | Threshold | Status |
|--------|----------|-----------|--------|
| Statements | ${percentages.statements.toFixed(1)}% | ${COVERAGE_THRESHOLDS.statements}% | ${thresholdsMet.statements ? '✅' : '❌'} |
| Branches | ${percentages.branches.toFixed(1)}% | ${COVERAGE_THRESHOLDS.branches}% | ${thresholdsMet.branches ? '✅' : '❌'} |
| Functions | ${percentages.functions.toFixed(1)}% | ${COVERAGE_THRESHOLDS.functions}% | ${thresholdsMet.functions ? '✅' : '❌'} |
| Lines | ${percentages.lines.toFixed(1)}% | ${COVERAGE_THRESHOLDS.lines}% | ${thresholdsMet.lines ? '✅' : '❌'} |

**Files Analyzed:** ${summary.fileCount}
`;
    }

    /**
     * Format category analysis
     */
    formatCategoryAnalysis(categoryAnalysis) {
        let output = '';

        Object.entries(categoryAnalysis).forEach(([category, data]) => {
            const avgCoverage = this.getAverageCoverage(data);
            const status =
                avgCoverage >= 75 ? '✅' : avgCoverage >= 60 ? '⚠️' : '❌';

            output += `
### ${category} ${status}

- **Average Coverage:** ${avgCoverage.toFixed(1)}%
- **Files:** ${data.fileCount}
- **Top Issues:** ${this.getTopIssuesForCategory(data)}

`;
        });

        return output;
    }

    /**
     * Format file analysis
     */
    formatFileAnalysis(fileAnalysis) {
        const topFiles = fileAnalysis.slice(0, 10);

        let output = `
### Files Needing Attention

| File | Statements | Branches | Functions | Lines | Priority |
|------|------------|----------|-----------|-------|----------|
`;

        topFiles.forEach((file) => {
            const metrics = file.metrics;
            const priority =
                this.getAverageCoverage(metrics) < 50
                    ? 'High'
                    : this.getAverageCoverage(metrics) < 70
                      ? 'Medium'
                      : 'Low';

            output += `| ${file.path} | ${metrics.statements?.percentage.toFixed(1) || 'N/A'}% | ${metrics.branches?.percentage.toFixed(1) || 'N/A'}% | ${metrics.functions?.percentage.toFixed(1) || 'N/A'}% | ${metrics.lines?.percentage.toFixed(1) || 'N/A'}% | ${priority} |\n`;
        });

        return output;
    }

    /**
     * Format recommendations
     */
    formatRecommendations(recommendations) {
        if (recommendations.length === 0) {
            return '🎉 **All coverage thresholds met! No immediate recommendations.**';
        }

        let output = '';
        const groupedRecs = this.groupBy(recommendations, 'priority');

        ['high', 'medium', 'low'].forEach((priority) => {
            if (groupedRecs[priority]) {
                output += `\n### ${priority.toUpperCase()} Priority\n\n`;
                groupedRecs[priority].forEach((rec, index) => {
                    output += `${index + 1}. ${rec.message}\n`;
                });
            }
        });

        return output;
    }

    /**
     * Format trends (placeholder for historical data)
     */
    formatTrends() {
        return `
*Historical trend analysis would appear here with previous coverage data.*

To enable trend analysis:
1. Store coverage results after each test run
2. Compare with previous results
3. Track coverage changes over time
`;
    }

    /**
     * Format test quality metrics
     */
    formatTestQualityMetrics() {
        const testFiles = this.getTestFileCount();
        const sourceFiles = Object.keys(this.coverageData).length;
        const testRatio = sourceFiles > 0 ? testFiles / sourceFiles : 0;

        return `
| Metric | Value |
|--------|-------|
| Test Files | ${testFiles} |
| Source Files | ${sourceFiles} |
| Test-to-Source Ratio | ${testRatio.toFixed(2)}:1 |
| Recommended Ratio | 1:1 or higher |
`;
    }

    /**
     * Format action items
     */
    formatActionItems(summary, fileAnalysis) {
        const actions = [];

        if (!summary.thresholdsMet.all) {
            actions.push(
                '📈 **Increase overall test coverage** to meet minimum thresholds'
            );
        }

        const criticalFiles = fileAnalysis.filter(
            (file) => this.getAverageCoverage(file.metrics) < 50
        );

        if (criticalFiles.length > 0) {
            actions.push(
                `🎯 **Focus on ${criticalFiles.length} critical files** with very low coverage`
            );
        }

        const complexFiles = fileAnalysis.filter(
            (file) => file.complexity > 10
        );
        if (complexFiles.length > 0) {
            actions.push(
                `🔧 **Review ${complexFiles.length} complex files** for better testability`
            );
        }

        if (actions.length === 0) {
            actions.push(
                '✨ **Maintain current coverage levels** and continue monitoring'
            );
        }

        return actions
            .map((action, index) => `${index + 1}. ${action}`)
            .join('\n');
    }

    /**
     * Helper methods
     */
    checkThresholds(percentages) {
        const results = {};
        Object.entries(COVERAGE_THRESHOLDS).forEach(([metric, threshold]) => {
            results[metric] = percentages[metric] >= threshold;
        });
        results.all = Object.values(results).every(Boolean);
        return results;
    }

    getFilesByPatterns(patterns) {
        const allFiles = Object.keys(this.coverageData);
        return allFiles.filter((file) =>
            patterns.some((pattern) => file.includes(pattern))
        );
    }

    calculateCoverageForFiles(files) {
        const totals = {
            statements: { covered: 0, total: 0 },
            branches: { covered: 0, total: 0 },
            functions: { covered: 0, total: 0 },
            lines: { covered: 0, total: 0 },
        };

        files.forEach((filePath) => {
            const file = this.coverageData[filePath];
            if (file) {
                Object.keys(totals).forEach((metric) => {
                    if (file[metric]) {
                        totals[metric].covered += file[metric].covered || 0;
                        totals[metric].total += file[metric].total || 0;
                    }
                });
            }
        });

        const percentages = {};
        Object.keys(totals).forEach((metric) => {
            const { covered, total } = totals[metric];
            percentages[metric] = total > 0 ? (covered / total) * 100 : 0;
        });

        return percentages;
    }

    getFileCoverage(filePath) {
        const file = this.coverageData[filePath];
        if (!file) return {};

        const coverage = {};
        ['statements', 'branches', 'functions', 'lines'].forEach((metric) => {
            if (file[metric]) {
                const { covered, total } = file[metric];
                coverage[metric] = total > 0 ? (covered / total) * 100 : 0;
            }
        });

        return coverage;
    }

    getUncoveredLines(coverage) {
        if (!coverage.statementMap || !coverage.s) return [];

        const uncovered = [];
        Object.entries(coverage.s).forEach(([key, covered]) => {
            if (covered === 0 && coverage.statementMap[key]) {
                const location = coverage.statementMap[key];
                uncovered.push(location.start.line);
            }
        });

        return [...new Set(uncovered)].sort((a, b) => a - b);
    }

    estimateComplexity(coverage) {
        // Simple complexity estimation based on branches and functions
        const branches = coverage.branches?.total || 0;
        const functions = coverage.functions?.total || 0;
        return Math.ceil((branches + functions) / 2);
    }

    getAverageCoverage(metrics) {
        const values = Object.values(metrics)
            .filter((metric) => metric && typeof metric.percentage === 'number')
            .map((metric) => metric.percentage);

        return values.length > 0
            ? values.reduce((sum, val) => sum + val, 0) / values.length
            : 0;
    }

    getTopIssuesForCategory(data) {
        const issues = [];
        const avgCoverage = this.getAverageCoverage(data);

        if (avgCoverage < 60) {
            issues.push('Low overall coverage');
        }

        const lowCoverageFiles = data.files.filter(
            (file) => this.getAverageCoverage(file.coverage) < 50
        );

        if (lowCoverageFiles.length > 0) {
            issues.push(
                `${lowCoverageFiles.length} files with critical coverage`
            );
        }

        return issues.length > 0 ? issues.join(', ') : 'No major issues';
    }

    getTestFileCount() {
        try {
            const testDir = path.join(process.cwd(), 'src/tests');
            const countTestFiles = (dir) => {
                let count = 0;
                const items = fs.readdirSync(dir);

                items.forEach((item) => {
                    const fullPath = path.join(dir, item);
                    const stat = fs.statSync(fullPath);

                    if (stat.isDirectory()) {
                        count += countTestFiles(fullPath);
                    } else if (
                        item.endsWith('.test.ts') ||
                        item.endsWith('.test.tsx')
                    ) {
                        count++;
                    }
                });

                return count;
            };

            return countTestFiles(testDir);
        } catch (error) {
            return 0;
        }
    }

    groupBy(array, key) {
        return array.reduce((groups, item) => {
            const group = item[key];
            groups[group] = groups[group] || [];
            groups[group].push(item);
            return groups;
        }, {});
    }

    /**
     * Write report to file
     */
    async writeReport(report) {
        try {
            // Ensure coverage directory exists
            if (!fs.existsSync(this.reportPath)) {
                fs.mkdirSync(this.reportPath, { recursive: true });
            }

            fs.writeFileSync(this.outputPath, report, 'utf8');
        } catch (error) {
            throw new Error(`Failed to write report: ${error.message}`);
        }
    }

    /**
     * Display summary in console
     */
    displaySummary() {
        const summary = this.calculateSummary();
        const status = summary.thresholdsMet.all ? '✅ PASSING' : '❌ FAILING';

        console.log('\n📊 Coverage Summary:');
        console.log(`Status: ${status}`);
        console.log(`Files: ${summary.fileCount}`);
        console.log(
            `Statements: ${summary.percentages.statements.toFixed(1)}%`
        );
        console.log(`Branches: ${summary.percentages.branches.toFixed(1)}%`);
        console.log(`Functions: ${summary.percentages.functions.toFixed(1)}%`);
        console.log(`Lines: ${summary.percentages.lines.toFixed(1)}%`);
    }
}

// Run the coverage report generator
if (require.main === module) {
    const generator = new CoverageReportGenerator();
    generator.generateReport().catch((error) => {
        console.error('Failed to generate coverage report:', error);
        process.exit(1);
    });
}

module.exports = CoverageReportGenerator;
